# @lc code=start
class Solution(object):
    def bulbSwitch(self, n):
        """
        :type n: int
        :rtype: int
        """
        num_lights_on = 0

        if n == 0:
            num_lights_on = 0
        
        i = n
        while i != n:

            i -= 1

        return num_lights_on

